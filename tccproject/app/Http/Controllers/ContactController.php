            'data' => 'required',
            'nome' => 'required',
            'ocorrencia' => 'required'
        ]);

        $data = array(
            'data' => $request->data,
            'nome' => $request->nome,
            'ocorrencia' => $request->ocorrencia
        );

        Mail::to(config('mail.from.address'))
            ->send( new SendMail($data));

